# - 부호로 먼저 잘라서 부분합을 구하면 시작, 끝만 양수. 나머진 음수
n = input()
lst = n.split('-')
arr = []

for i in lst:
    j = i.split('+')
    if len(j) > 1:
        j = [int(x) for x in j]
        k = sum(j)
    else:
        k = int(j[0])
    arr.append(k)

tot = arr[0]
tot -= sum(arr[1:])
print(tot)